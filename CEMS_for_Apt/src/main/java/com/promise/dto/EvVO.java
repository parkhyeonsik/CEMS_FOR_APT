package com.promise.dto;

public class EvVO {

	private String ev_id;
	private String ev_model;
	private String ev_num;
	private String ev_img;
	private String ev_owner;
	
	
	public String getEv_id() {
		return ev_id;
	}
	public void setEv_id(String ev_id) {
		this.ev_id = ev_id;
	}
	public String getEv_model() {
		return ev_model;
	}
	public void setEv_model(String ev_model) {
		this.ev_model = ev_model;
	}
	public String getEv_num() {
		return ev_num;
	}
	public void setEv_num(String ev_num) {
		this.ev_num = ev_num;
	}
	public String getEv_img() {
		return ev_img;
	}
	public void setEv_img(String ev_img) {
		this.ev_img = ev_img;
	}
	public String getEv_owner() {
		return ev_owner;
	}
	public void setEv_owner(String ev_owner) {
		this.ev_owner = ev_owner;
	}
	
	
}
