package com.promise.dto;

public class AptVO {

	private String apt_num;
	private String apt_address;
	private String apt_name;
	
	public String getApt_num() {
		return apt_num;
	}
	public void setApt_num(String apt_num) {
		this.apt_num = apt_num;
	}
	public String getApt_address() {
		return apt_address;
	}
	public void setApt_address(String apt_address) {
		this.apt_address = apt_address;
	}
	public String getApt_name() {
		return apt_name;
	}
	public void setApt_name(String apt_name) {
		this.apt_name = apt_name;
	}
	
	
}
